#include <bits/stdc++.h>

using namespace std;

int main()
{
    int t;
    cin>>t;
    char m;
    while(t--)
    {
        cin>>m;
        if(m=='c' || m=='o' || m=='d' || m=='e' || m=='f' || m=='r' || m=='s')
        {
            cout<<"YES"<<endl;
        }else
        {
            cout<<"NO"<<endl;
        }


    }
    return 0;
}
